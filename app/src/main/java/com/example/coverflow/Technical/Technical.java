package com.example.coverflow.Technical;

public class Technical {

    private String title;
    private String summary;
    private String Task;
    private String judcriteria;
    private String coordinators;
    private int image;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getSummary() {
        return summary;
    }

    public void setSummary(String summary) {
        this.summary = summary;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getTask() {
        return Task;
    }

    public void setTask(String task) {
        Task = task;
    }

    public String getJudcriteria() {
        return judcriteria;
    }

    public void setJudcriteria(String judcriteria) {
        this.judcriteria = judcriteria;
    }

    public String getCoordinators() {
        return coordinators;
    }

    public void setCoordinators(String coordinators) {
        this.coordinators = coordinators;
    }
}
